def merge_sort(A, p, r):
    """
    Основна рекурсивна процедура сортування злиттям.
    A: масив, p: початковий індекс, r: кінцевий індекс.
    """
    # Якщо ділянка містить більше одного елемента [cite: 31]
    if p < r:
        # 1. Поділ: обчислюємо середній індекс [cite: 32]
        q = (p + r) // 2
        
        # 2. Підкорення: рекурсивно сортуємо дві половини [cite: 34, 35]
        merge_sort(A, p, q)
        merge_sort(A, q + 1, r)
        
        # 3. Комбінування: зливаємо відсортовані частини [cite: 37]
        merge(A, p, q, r)

def merge(A, p, q, r):
    """
    Допоміжна процедура для злиття двох впорядкованих ділянок A[p..q] та A[q+1..r][cite: 16, 17].
    """
    # Створюємо тимчасові копії лівої та правої частин
    left_part = A[p : q + 1]
    right_part = A[q + 1 : r + 1]
    
    i = 0  # Індекс для лівої частини
    j = 0  # Індекс для правої частини
    k = p  # Індекс для основного масиву
    
    # Порівнюємо елементи та зливаємо їх у порядку зростання [cite: 23]
    while i < len(left_part) and j < len(right_part):
        if left_part[i] <= right_part[j]:
            A[k] = left_part[i]
            i += 1
        else:
            A[k] = right_part[j]
            j += 1
        k += 1
    
    # Додаємо залишки елементів, якщо одна зі стопок порожня [cite: 24]
    while i < len(left_part):
        A[k] = left_part[i]
        i += 1
        k += 1
        
    while j < len(right_part):
        A[k] = right_part[j]
        j += 1
        k += 1

